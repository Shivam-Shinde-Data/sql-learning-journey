-- 🟡 Q4: Customers with Latest Order After Feb 2023
-- Find customers whose latest order date is after '2023-02-01'.
-- 👉 Think aggregation + filtering

SELECT customer_id, MAX(order_date) AS latest_order
FROM orders
GROUP BY customer_id
HAVING MAX(order_date) > DATE '2023-02-01';



-- 🟡 Q5: Customers Who Never Cancelled Any Order
-- Find customers who have placed orders but never cancelled.
-- 👉 Trick: exclusion logic

SELECT customer_id
FROM orders
GROUP BY customer_id
HAVING SUM(CASE WHEN order_status = 'CANCELLED' THEN 1 ELSE 0 END) = 0;



-- 🔴 Q6: Customers Whose Spend is Above Their Country Average
-- Find customers whose total spend is greater than average spend of their country.
-- 👉 Multi-level aggregation 🔥

WITH customer_spend AS (
    SELECT c.customer_id, c.country, SUM(o.order_amount) AS total_spend
    FROM customers c
    JOIN orders o
    ON c.customer_id = o.customer_id
    GROUP BY c.customer_id, c.country
),
country_avg AS (
    SELECT country, AVG(total_spend) AS avg_country_spend
    FROM customer_spend
    GROUP BY country
)
SELECT cs.customer_id, cs.country, cs.total_spend
FROM customer_spend cs
JOIN country_avg ca
ON cs.country = ca.country
WHERE cs.total_spend > ca.avg_country_spend;



