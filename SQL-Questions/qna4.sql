-- 🔴 Q8: Customers with More Orders Than Average Customer
-- Find customers whose total number of orders is greater than average orders per customer.

WITH order_counts AS (
    SELECT customer_id, COUNT(*) AS total_orders
    FROM orders
    GROUP BY customer_id
),
avg_orders AS (
    SELECT AVG(total_orders) AS avg_order_count
    FROM order_counts
)
SELECT oc.customer_id, oc.total_orders
FROM order_counts oc
JOIN avg_orders a
ON oc.total_orders > a.avg_order_count;



-- 🔴 Q9: Revenue of Top 2 Customers
-- Find total revenue generated by top 2 highest spending customers.
-- 👉 Step-wise thinking required

WITH customer_spend AS (
    SELECT customer_id, SUM(order_amount) AS total_spend
    FROM orders
    GROUP BY customer_id
),
ranked_customers AS (
    SELECT customer_id, total_spend,
           RANK() OVER (ORDER BY total_spend DESC) AS rnk
    FROM customer_spend
)
SELECT SUM(total_spend) AS total_revenue
FROM ranked_customers
WHERE rnk <= 2;



-- 🔴 Q10: Customers Who Contribute to Top 50% Revenue
-- Find customers who contribute to top 50% of total revenue.

WITH customer_spend AS (
    SELECT customer_id, SUM(order_amount) AS total_spend
    FROM orders
    GROUP BY customer_id
),
total_revenue AS (
    SELECT SUM(total_spend) AS total_rev
    FROM customer_spend
),
ranked AS (
    SELECT cs.customer_id, cs.total_spend,
           SUM(cs.total_spend) OVER (ORDER BY cs.total_spend DESC) AS running_total
    FROM customer_spend cs
)
SELECT r.customer_id, r.total_spend
FROM ranked r
JOIN total_revenue t
ON r.running_total <= 0.5 * t.total_rev;
