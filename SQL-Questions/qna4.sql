-- 🟢 Q1: Customers Above Average Spend
-- Find customers whose total spend is greater than average customer spend.
-- 👉 Multi-step required

WITH customer_spend AS (
    SELECT customer_id, SUM(order_amount) AS total_spend
    FROM orders
    GROUP BY customer_id
),
avg_spend AS (
    SELECT AVG(total_spend) AS avg_total_spend
    FROM customer_spend
)
SELECT cs.customer_id, cs.total_spend
FROM customer_spend cs
JOIN avg_spend a
ON cs.total_spend > a.avg_total_spend;



-- 🟢 Q2: Orders Above Overall Average Amount
-- Find all orders where order_amount is greater than average order amount.

SELECT *
FROM orders
WHERE order_amount > (
    SELECT AVG(order_amount)
    FROM orders
);



-- 🟡 Q3: Top Spending Customers (Above 1000)
-- Find customers whose total spend is greater than 1000.
-- 👉 Solve using:
-- Subquery

SELECT customer_id, SUM(order_amount) AS total_spend
FROM orders
GROUP BY customer_id
HAVING SUM(order_amount) > 1000;

-- 👉 Using CTE

WITH customer_spend AS (
    SELECT customer_id, SUM(order_amount) AS total_spend
